'''
let's write a program to find gcd of two numbers.

we will import math module
and use math.gcd() function
'''
import math
num1 = int(input("enter the first number: "))

num2 = int(input("enter the second number: "))

gcd = math.gcd(num1, num2)

print("GCD is : ", gcd)

