'''
lets write a program to print multiplication table of any given number

n x 1 = m1
n x 2 = m2
........
n x 10 = m10
like this
'''


n = int(input("enter the number: "))

for i in range(1, 11): # range() function runs from lowerlimit to upperLimit-1
    print(n, " x ", i , " = ", n*i)

